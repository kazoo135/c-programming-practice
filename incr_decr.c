#include <stdio.h>
#include <stdlib.h>

/*testing out increment and decrement operators
post and pre */

int main(){
    int x = 10;
    int y = 39;
    printf("x is %d\n", x++);
    printf("x psot increment is %d\n", x);
    printf("y is %d\n", y--);
    printf("now y is %d\n", y);

    return 0;
}
